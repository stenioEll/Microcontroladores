		CONT		; PARA LOOP
		VAL_TROCA	; VAR COM O VALOR QUE SERÁ TROCADO DE POSIÇÃO
		DECREMENTO  
		PIVO
		TROCAFLAG
		

		;NOVAS VARIÁVEIS

	ENDC			;FIM DO BLOCO DE MEMÓRIA
