;* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
;*                     ROTINA PRINCIPAL                            *
;* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * *
MAIN
	
	;CORPO DA ROTINA PRINCIPAL
	;MOVLW .1
	;MOVWF DECREMENTO    ; SÓ PARA DECREMENTAR INDIRETAMENTE
	CLRF TROCAFLAG       ; ZERANDO A FLAG
	MOVLW .0
	MOVWF CONT	    ; ESSA VAR GUARDARÁ OS INDICES DAS POSIÇÕES, EM SUA PRIMEIRA EXECUÇÃO SERÁ A POSIÇÃO 0
	MOVFW CONT
	GOTO LEITURA
LEITURA
	CALL LE_EEPROM         
	MOVWF DADO	    ;SALVANDO O VALOR DO ELEMENTO QUE ESTÁ NA PRIMEIRA POSIÇÃO DA EPROM
	GOTO LOOP	    
LOOP	
	CLRF W
	INCF CONT	  ; INCREMENTA PARA O ENDEREÇO DA POSIÇÃO N+1
	MOVFW CONT	  ; MOVENDO PARA O WORK
	CALL LE_EEPROM
	MOVWF AUX	  ; MOVI PARA UMA VARIÁVEL AUXILIAR QUE AJUDARÁ NA COMPARAÇÃO
	GOTO COMPARA
COMPARA
	MOVFW AUX	  ; MOVENDO O VALOR DA POSIÇÃO N+1 PARA O WORK PARA FAZER A SUBTRAÇÃO
	SUBWF DADO, W     ; SE AUX FOR MENOR QUE DADO, O BIT C SERÁ IGUAL A 1
	BTFSC STATUS, Z   ; SE FOR IGUAL PERMANECE
	GOTO FIM
	BTFSC STATUS, C	  ; SE C = 0, O ELEMENTO DA POSIÇÃO N É MENOR QUE N+1!
	GOTO ORDENA1
	GOTO PERMANECE
	
	
	
ORDENA1 
	INCF TROCAFLAG       ; SETANDO A VARIÁVEL TROCA 
	MOVFW AUX            ; MOVENDO O VALOR Q ESTÁ CONTIDO NA POSIÇÃO N+1(AUX) PARA OUTRA VARIÁVEL
	MOVWF VAL_TROCA
	DECF CONT            ; MOVENDO A POSIÇÃO N PARA O WORK
	MOVFW CONT
	CALL GRAVA_EEPROM
	MOVFW DADO           ; MOVENDO O VALOR Q ESTÁ CONTIDO NA VAR DADO REFERENTE A ANTIGA POSIÇÃO N PARA OUTRA VARIÁVEL
	MOVWF VAL_TROCA
	INCF CONT            ; MOVENDO A POSIÇÃO N+1 PARA O WORK
	MOVFW CONT
	CALL GRAVA_EEPROM
	GOTO FIMLOOP
PERMANECE          
	MOVFW AUX         ;O ELEMENTO DA POSIÇÃO PERMANECE, MAS PARA NÃO PERDER O VALOR AO VOLTAR PARA O LOOP ATUALIZO DADO COM O VALOR N+1
	MOVWF DADO
	GOTO FIMLOOP
FIMLOOP
	MOVLW .39          ;MÁXIMO DE POSIÇÕES 
	SUBWF CONT, W	   ; SE FOR POSITIVO PERCORREMOS TODAS AS POSIÇÕES, SE NÃO O PIVÔ CONTINUA 
	BTFSC STATUS, C
	GOTO TROCA
	GOTO LOOP

TROCA
	MOVFW TROCAFLAG
	BTFSC STATUS, Z
	GOTO  FIM
	GOTO MAIN

	
	
	
	
	 
